@lru_cache(maxsize=1)
def load_class_labels_from_hub(
    repo_id: str = "AllanK24/apple-dms-materials"
) -> Dict[str, Any]:
    """
    Load class labels from HuggingFace Hub.
    
    Args:
        repo_id: HuggingFace Hub repository ID
    
    Returns:
        Dictionary with id2label, label2id, num_labels, and optionally colormap
    """
    from huggingface_hub import hf_hub_download
    
    try:
        class_info_path = hf_hub_download(
            repo_id=repo_id,
            filename="class_info.json",
            repo_type="dataset",
        )
        with open(class_info_path, "r") as f:
            class_info = json.load(f)
        
        # Convert string keys back to int for id2label
        id2label = {int(k): v for k, v in class_info["id2label"].items()}
        label2id = class_info["label2id"]
        
        return {
            "id2label": id2label,
            "label2id": label2id,
            "num_labels": class_info["num_labels"],
            "colormap": class_info.get("colormap"),
        }
    except Exception as e:
        print(f"Warning: Could not load class info from Hub: {e}")
        print("Falling back to default class labels.")
        return _get_default_class_labels()


def load_class_labels_from_local(data_path: str) -> Dict[str, Any]:
    """
    Load class labels from local taxonomy.json file.
    
    Args:
        data_path: Path to the DMS_v1 directory containing taxonomy.json
    
    Returns:
        Dictionary with id2label, label2id, num_labels, and colormap
    """
    taxonomy_path = os.path.join(data_path, "taxonomy.json")
    
    if not os.path.exists(taxonomy_path):
        # Try class_info.json (generated by push_to_hub.py --dry_run)
        class_info_path = os.path.join(data_path, "class_info.json")
        if os.path.exists(class_info_path):
            with open(class_info_path, "r") as f:
                class_info = json.load(f)
            id2label = {int(k): v for k, v in class_info["id2label"].items()}
            return {
                "id2label": id2label,
                "label2id": class_info["label2id"],
                "num_labels": class_info["num_labels"],
                "colormap": class_info.get("colormap"),
            }
        raise FileNotFoundError(
            f"Neither taxonomy.json nor class_info.json found in {data_path}"
        )
    
    with open(taxonomy_path, "r") as f:
        taxonomy = json.load(f)
    
    names = taxonomy["names"]
    id2label = {i: name for i, name in enumerate(names)}
    label2id = {name: i for i, name in enumerate(names)}
    
    return {
        "id2label": id2label,
        "label2id": label2id,
        "num_labels": len(names),
        "colormap": taxonomy.get("srgb_colormap", taxonomy.get("colormap")),
    }


