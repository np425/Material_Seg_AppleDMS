# Convenience accessors - lazy loaded
_cached_labels: Optional[Dict[str, Any]] = None

def get_class_labels(
    source: str = "hub",
    data_path: Optional[str] = None,
    repo_id: str = "AllanK24/apple-dms-materials",
) -> Dict[str, Any]:
    """
    Get class label mappings.
    
    Args:
        source: "hub" to load from HuggingFace, "local" for local files, "default" for fallback
        data_path: Required if source="local"
        repo_id: Hub repo ID if source="hub"
    
    Returns:
        Dictionary with id2label, label2id, num_labels, colormap
    """
    global _cached_labels
    
    if _cached_labels is not None:
        return _cached_labels
    
    if source == "hub":
        _cached_labels = load_class_labels_from_hub(repo_id)
    elif source == "local":
        if data_path is None:
            raise ValueError("data_path required when source='local'")
        _cached_labels = load_class_labels_from_local(data_path)
    else:
        _cached_labels = _get_default_class_labels()
    
    return _cached_labels


# For backwards compatibility - these will be populated on first access
