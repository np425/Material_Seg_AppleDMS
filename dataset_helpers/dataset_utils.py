def get_dms_datasets(
    source: str = "hub",
    data_path: Optional[str] = None,
    repo_id: str = "AllanK24/apple-dms-materials",
    processor: Optional[ImageProcessor] = None,
    augment_train: bool = False,
    model_type: str = "segformer",
    crop_size: Tuple[int, int] = (512, 512),
) -> Dict[str, Dataset]:
    """
    Convenience function to load Apple-DMS datasets.
    
    Args:
        source: Either "hub" to load from HuggingFace or "local" for local files
        data_path: Required if source="local", path to DMS_v1 directory
        repo_id: HuggingFace Hub repo ID (only used if source="hub")
        processor: Optional image processor (SegFormer or Mask2Former)
        augment_train: If True, apply material-specific augmentations to train split
        model_type: "segformer" or "mask2former" — determines augmentation config
        crop_size: Output crop size for LSJ augmentation
    
    Returns:
        Dictionary with train, validation, test datasets
    """
    if source == "hub":
        return load_dms_from_hub(repo_id, processor, augment_train, model_type, crop_size)
    elif source == "local":
        if data_path is None:
            raise ValueError("data_path is required when source='local'")
        return load_dms_from_local(data_path, processor)
    else:
        raise ValueError(f"Unknown source: {source}. Use 'hub' or 'local'")


