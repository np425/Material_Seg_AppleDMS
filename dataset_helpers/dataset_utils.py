# Image Processor
# ============================================================================

def get_image_processor(
    model_name: str = "nvidia/segformer-b5-finetuned-ade-640-640",
    size: Optional[Dict[str, int]] = None,
    do_reduce_labels: bool = False,
) -> ImageProcessor:
    """
    Get an image processor for the specified model.
    
    Automatically detects the model type and returns the appropriate processor:
    - SegFormer models: Returns SegformerImageProcessor
    - Mask2Former models: Returns Mask2FormerImageProcessor
    
    Args:
        model_name: Pretrained model to get processor config from
        size: Optional custom size dict, e.g., {"height": 512, "width": 512}
        do_reduce_labels: Whether to reduce labels (subtract 1, useful when 0 is background)
    
    Returns:
        Image processor configured for the specified model
    """
    # Use AutoImageProcessor to automatically get the correct processor type
    if "mask2former" in model_name.lower():
        # Use slow processor to avoid grouping issues with variable-size images (Mask2Former)
        processor = AutoImageProcessor.from_pretrained(model_name, use_fast=False)
    else:
        processor = AutoImageProcessor.from_pretrained(model_name)
    
    if size is not None:
        processor.size = size
    
    # Set reduce_labels if the processor supports it
    if hasattr(processor, 'do_reduce_labels'):
        processor.do_reduce_labels = do_reduce_labels

    return processor


# ============================================================================
# Dataset Class for Local Files
# ============================================================================

class DMSSegmentationDataset(Dataset):
    """
    PyTorch Dataset for Apple-DMS material segmentation.
    
    Works with both local files and HuggingFace datasets.
    Compatible with both SegFormer and Mask2Former image processors.
    """
    
    def __init__(
        self,
        image_paths: List[str],
        label_paths: List[str],
        processor: Optional[ImageProcessor] = None,
        transform: Optional[Any] = None,
    ):
        """
        Initialize the dataset.
        
        Args:
            image_paths: List of paths to input images
            label_paths: List of paths to segmentation masks
            processor: Image processor for preprocessing (SegFormer or Mask2Former)
            transform: Optional additional transforms
        """
        assert len(image_paths) == len(label_paths), \
            f"Mismatch: {len(image_paths)} images vs {len(label_paths)} labels"
        
        self.image_paths = image_paths
        self.label_paths = label_paths
        self.processor = processor or get_image_processor()
        self.transform = transform
    
    def __len__(self) -> int:
        return len(self.image_paths)
    
    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        # Load image and label
        image = Image.open(self.image_paths[idx]).convert("RGB")
        label = Image.open(self.label_paths[idx])
        
        # Apply optional transforms
        if self.transform is not None:
            image, label = self.transform(image, label)
        
        # Process with image processor (SegFormer or Mask2Former)
        encoding = self.processor(
            images=image,
            segmentation_maps=label,
            return_tensors="pt",
        )
        
        # Build output dict - handle different processor output formats
        result = {"pixel_values": encoding["pixel_values"].squeeze(0)}
        
        # SegformerImageProcessor returns "labels"
        # Mask2FormerImageProcessor returns "mask_labels" and "class_labels"
        if "labels" in encoding:
            result["labels"] = encoding["labels"].squeeze(0)
        if "mask_labels" in encoding:
            result["mask_labels"] = encoding["mask_labels"][0]  # Remove batch dim
        if "class_labels" in encoding:
            result["class_labels"] = encoding["class_labels"][0]  # Remove batch dim
        
        return result


