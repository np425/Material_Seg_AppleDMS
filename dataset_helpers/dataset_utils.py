"""
Dataset utilities for Apple-DMS material segmentation.

This module provides:
- DMSSegmentationDataset: A torch Dataset class for local files
- Helper functions to load from HuggingFace Hub
- Preprocessing transforms compatible with both SegFormer and Mask2Former
- Class label mappings (57 material classes, loaded dynamically)

Supported models:
- SegFormer: Uses SegformerImageProcessor
- Mask2Former: Uses Mask2FormerImageProcessor (via AutoImageProcessor)
"""

