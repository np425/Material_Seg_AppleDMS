def load_dms_from_hub(
    repo_id: str = "AllanK24/apple-dms-materials",
    processor: Optional[ImageProcessor] = None,
    augment_train: bool = False,
    model_type: str = "segformer",
    crop_size: Tuple[int, int] = (512, 512),
) -> Dict[str, HFDatasetWrapper]:
    """
    Load Apple-DMS dataset from HuggingFace Hub.
    
    Args:
        repo_id: HuggingFace Hub repository ID
        processor: Optional image processor (SegFormer or Mask2Former)
        augment_train: If True, apply material-specific augmentations to the
                       training split. Default False (no augmentations).
        model_type: "segformer" or "mask2former" — determines augmentation config.
                    Only used when augment_train=True.
        crop_size: Output crop size for LSJ augmentation. Default (512, 512).
    
    Returns:
        Dictionary with train, validation, test dataset wrappers
    """
    from datasets import load_dataset
    
    print(f"Loading dataset from Hub: {repo_id}")
    dataset = load_dataset(repo_id)
    
    if processor is None:
        processor = get_image_processor()
    
    # Also load class labels from Hub
    get_class_labels(source="hub", repo_id=repo_id)
    
    # Create augmentation for training split if requested
    train_augmentation = None
    if augment_train:
        import sys as _sys
        from pathlib import Path as _Path
        # augmentations.py lives in training/segformer/ — add it to path if needed
        _augmentations_dir = str(_Path(__file__).resolve().parent.parent / "training" / "segformer")
        if _augmentations_dir not in _sys.path:
            _sys.path.insert(0, _augmentations_dir)
        from augmentations import get_material_augmentation
        train_augmentation = get_material_augmentation(
            model_type=model_type,
            crop_size=crop_size,
        )
        print(f"  Augmentations enabled for training ({model_type}):")
        print(f"  {train_augmentation}")
    
    wrapped = {}
    for split in ["train", "validation", "test"]:
        if split in dataset:
            # Only train split gets augmentation; val/test never augmented
            aug = train_augmentation if split == "train" else None
            wrapped[split] = HFDatasetWrapper(dataset[split], processor, augmentation=aug)
            aug_str = " (augmented)" if aug is not None else ""
            print(f"  {split}: {len(wrapped[split])} samples{aug_str}")
    
    return wrapped


def load_dms_from_local(
    data_path: str,
    processor: Optional[ImageProcessor] = None,
) -> Dict[str, DMSSegmentationDataset]:
    """
    Load Apple-DMS dataset from local files.
    
    Args:
        data_path: Path to the DMS_v1 directory
        processor: Optional image processor (SegFormer or Mask2Former)
    
    Returns:
        Dictionary with train, validation, test datasets
    """
    import gzip
    
    # Load metadata
    info_path = os.path.join(data_path, "info.json.gz")
    with gzip.open(info_path, "rb") as f:
        data = json.loads(f.read())
    
    if processor is None:
        processor = get_image_processor()
    
    # Load class labels from local taxonomy
    get_class_labels(source="local", data_path=data_path)
    
    # Organize by split
    splits = {"train": ([], []), "validation": ([], []), "test": ([], [])}
    
    for datum in data:
        label_path = datum.get("label_path", "")
        image_path = datum.get("image_path", "")
        
        # Determine split from label path
        split = None
        for s in ["train", "validation", "test"]:
            if s in label_path:
                split = s
                break
        
        if split is None:
            continue
        
        full_image_path = os.path.join(data_path, image_path)
        full_label_path = os.path.join(data_path, label_path)
        
        if os.path.exists(full_image_path) and os.path.exists(full_label_path):
            splits[split][0].append(full_image_path)
            splits[split][1].append(full_label_path)
    
    datasets = {}
    for split, (images, labels) in splits.items():
        if images:
            datasets[split] = DMSSegmentationDataset(images, labels, processor)
            print(f"  {split}: {len(datasets[split])} samples")
    
    return datasets


# ============================================================================
# Batch Transform Function (for use with HF Trainer's data_collator)
# ============================================================================

def transforms(batch, processor: Optional[ImageProcessor] = None):
    """
    Transform function for HuggingFace dataset.map() or batched processing.
    
    Args:
        batch: Batch from HF dataset with "image" and "label" keys
        processor: Image processor (SegFormer or Mask2Former)
    
    Returns:
        Dictionary with "pixel_values" and "labels"
    """
    if processor is None:
        processor = get_image_processor()
    
    images = [x.convert("RGB") for x in batch["image"]]
    labels = [x for x in batch["label"]]
    
    inputs = processor(images=images, segmentation_maps=labels, return_tensors="pt")
    return inputs


