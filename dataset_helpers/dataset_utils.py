# HuggingFace Dataset Wrapper
# ============================================================================

class HFDatasetWrapper(Dataset):
    """
    Wrapper for HuggingFace datasets to work with PyTorch DataLoader.
    
    This wraps a HuggingFace dataset split and applies preprocessing.
    Compatible with both SegFormer and Mask2Former image processors.
    Optionally applies material-specific augmentations before the processor.
    """
    
    def __init__(
        self,
        hf_dataset,
        processor: Optional[ImageProcessor] = None,
        augmentation=None,
    ):
        """
        Initialize the wrapper.
        
        Args:
            hf_dataset: A HuggingFace dataset split (e.g., dataset["train"])
            processor: Image processor for preprocessing (SegFormer or Mask2Former)
            augmentation: Optional MaterialAugmentation instance.
                          When provided, augmentation(image, label) is called
                          before the image processor. Should only be set for
                          the training split.
        """
        self.dataset = hf_dataset
        self.processor = processor or get_image_processor()
        self.augmentation = augmentation
    
    def __len__(self) -> int:
        return len(self.dataset)
    
    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        item = self.dataset[idx]
        
        # Get image and label (HF datasets return PIL images)
        image = item["image"].convert("RGB")
        label = item["label"]
        
        # Apply augmentations BEFORE the image processor
        # Augmentations handle geometric (image+label) and photometric (image only)
        if self.augmentation is not None:
            image, label = self.augmentation(image, label)
        
        # Process with image processor (SegFormer or Mask2Former)
        encoding = self.processor(
            images=image,
            segmentation_maps=label,
            return_tensors="pt",
        )
        
        # Build output dict - handle different processor output formats
        result = {"pixel_values": encoding["pixel_values"].squeeze(0)}
        
        # SegformerImageProcessor returns "labels"
        # Mask2FormerImageProcessor returns "mask_labels" and "class_labels"
        if "labels" in encoding:
            result["labels"] = encoding["labels"].squeeze(0)
        if "mask_labels" in encoding:
            # mask_labels is a list of tensors, one per image in batch
            result["mask_labels"] = encoding["mask_labels"][0]  # Remove batch dim
        if "class_labels" in encoding:
            # class_labels is a list of tensors, one per image in batch  
            result["class_labels"] = encoding["class_labels"][0]  # Remove batch dim
        
        return result


